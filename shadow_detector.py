# --- PHASE 3: LOGIT DIVERGENCE & CONFLICT ANALYZERS ---

def analyze_logit_divergence(prompt, response):
    combined_text = prompt + " " + response
    tokens = model.to_tokens(combined_text)
    str_tokens = model.to_str_tokens(combined_text)
    prompt_len = len(model.to_str_tokens(prompt))
    
    # Run the forward pass
    logits, _ = model.run_with_cache(tokens)
    
    print(f"\n--- Logit Divergence Analysis ---")
    print(f"{'Word':<15} | {'Chosen':<10} | {'Top Alternatives (Probability)':<40}")
    print("-" * 75)

    for i in range(prompt_len, len(str_tokens)):
        current_logits = logits[0, i-1, :]
        probs = F.softmax(current_logits, dim=-1)
        
        # Get Top 3 candidates
        top_values, top_indices = torch.topk(probs, 3)
        chosen_token = str_tokens[i]
        
        # Format the top 3 for display
        alts = []
        for val, idx in zip(top_values, top_indices):
            token_name = model.to_string(idx)
            alts.append(f"'{token_name}' ({val:.1%})")
        
        # Hallucination Check: High Divergence
        # If the gap between #1 and #2 is small, the model is 'Diverging'
        gap = top_values[0] - top_values[1]
        alert = "[!]" if gap < 0.1 else "   " # Alert if gap is less than 10%
        
        print(f"{alert} {chosen_token:<13} | {chosen_token.strip():<10} | {' vs '.join(alts)}")


def analyze_conflict(prompt, external_response):
    combined_text = prompt + " " + external_response
    tokens = model.to_tokens(combined_text)
    str_tokens = model.to_str_tokens(combined_text)
    prompt_len = len(model.to_str_tokens(prompt))
    
    # Forward pass to get Shadow AI's predictions
    logits, _ = model.run_with_cache(tokens)
    
    print(f"\n--- Model Conflict Report ---")
    print(f"{'Status':<18} | {'Gemini Said':<12} | {'Shadow AI Predicted instead...'}")
    print("-" * 70)

    for i in range(prompt_len, len(str_tokens)):
        actual_token_id = tokens[0, i]
        actual_token_str = str_tokens[i]
        
        shadow_probs = F.softmax(logits[0, i-1, :], dim=-1)
        
        # Conflict Score: Probability the Shadow AI gave to Gemini's word
        prob_of_actual = shadow_probs[actual_token_id].item()
        conflict_score = 1 - prob_of_actual
        
        # Get the Shadow AI's preferred word
        top_shadow_id = shadow_probs.argmax().item()
        top_shadow_str = model.to_string(top_shadow_id)
        
        # Flagging Logic
        status = "[OK]"
        if conflict_score > 0.95: status = "[HIGH CONFLICT]"
        elif conflict_score > 0.80: status = "[WARN]"

        print(f"{status:16} | {actual_token_str:12} | Shadow wanted: '{top_shadow_str.strip()}' ({shadow_probs[top_shadow_id]:.1%})")

# Test it with the Japan/Eiffel Tower hallucination
fake_response_2 = "The capital of Japan is Tokyo. It is known for the Eiffel Tower."

analyze_logit_divergence(user_prompt, fake_response_2)
analyze_conflict(user_prompt, fake_response_2)
